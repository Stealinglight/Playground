function calculateDaysBetweenDates(begin, end) {
    let beginDate = new Date(begin);
    let endDate = new Date(end);
    let timeDiff = Math.abs(endDate.getTime() - beginDate.getTime());
    return Math.ceil(timeDiff / (1000 * 3600 * 24));
}

